/* Includes ================================================================= */
#include "shell/shell.h"
#include "os/abort/abort.h"

/* Defines ================================================================== */
#define LOG_TAG SHELL

/* Macros =================================================================== */
/* Exposed macros =========================================================== */
/* Enums ==================================================================== */
/* Types ==================================================================== */
/* Variables ================================================================ */
/* Private functions ======================================================== */
/* Shared functions ========================================================= */
static int8_t cmd_abort(shell_t * sh, uint8_t argc, const char ** argv) {
  os_abort("%s", argc == 2 ? argv[1] : "Manual abort");
  return SHELL_OK;
}

SHELL_DECLARE_COMMAND(abort, cmd_abort, "Aborts app");

