/* Includes ================================================================= */
#include "shell/shell.h"

/* Defines ================================================================== */
#define LOG_TAG SHELL

/* Macros =================================================================== */
/* Exposed macros =========================================================== */
/* Enums ==================================================================== */
/* Types ==================================================================== */
/* Variables ================================================================ */
/* Private functions ======================================================== */
/* Shared functions ========================================================= */
static int8_t cmd_exit(shell_t * sh, uint8_t argc, const char ** argv) {
  shell_stop(sh);
  return SHELL_OK;
}

SHELL_DECLARE_COMMAND(exit, cmd_exit, "Exit from shell");
