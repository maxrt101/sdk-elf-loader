/* Includes ================================================================= */
#include "shell/shell.h"
#include "log/log.h"

/* Defines ================================================================== */
#define LOG_TAG SHELL

/* Macros =================================================================== */
/* Exposed macros =========================================================== */
/* Enums ==================================================================== */
/* Types ==================================================================== */
/* Variables ================================================================ */
/* Private functions ======================================================== */
/* Shared functions ========================================================= */
static int8_t cmd_echo(shell_t * sh, uint8_t argc, const char ** argv) {
  for (uint8_t i = 1; i < argc; ++i) {
    log_printf("%s ", argv[i]);
  }
  log_printf("\r\n");
  return SHELL_OK;
}

SHELL_DECLARE_COMMAND(echo, cmd_echo, "Echoes arguments");
