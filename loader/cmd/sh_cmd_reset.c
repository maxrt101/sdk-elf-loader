/* Macros =================================================================== */
/* Exposed macros =========================================================== */
/* Enums ==================================================================== */
/* Types ==================================================================== */
/* Variables ================================================================ */
/* Private functions ======================================================== */
/* Shared functions ========================================================= */
static int8_t cmd_reset(shell_t * sh, uint8_t argc, const char ** argv) {
  os_reset(OS_RESET_SOFT);
  return SHELL_OK;
}

SHELL_DECLARE_COMMAND(reset, cmd_reset, "Reset device (restart)");
