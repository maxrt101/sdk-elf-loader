/**
  * @brief This function handles RTC global interrupt through EXTI lines 17, 19 and 20 and LSE CSS interrupt through EXTI line 19.
  */
void RTC_IRQHandler(void)
{
  /* USER CODE BEGIN RTC_IRQn 0 */
  if (LL_RTC_IsActiveFlag_WUT(RTC)) {

#if USE_RTC_IRQ_DEBUG_LOG
    log_printf("RTC: %d\r\n", LL_RTC_WAKEUP_GetAutoReload(RTC) / 16);
#endif

    global_runtime += LL_RTC_WAKEUP_GetAutoReload(RTC) / 16;

    if (runtime_get() < global_runtime) {
      runtime_inc(global_runtime - runtime_get());
    }

    os_power_mode_change(OS_POWER_MODE_NORMAL);

    LL_RTC_ClearFlag_WUT(RTC);
    LL_PWR_ClearFlag_WU();
  }
  /* USER CODE END RTC_IRQn 0 */

  /* USER CODE BEGIN RTC_IRQn 1 */
  LL_EXTI_ClearFlag_0_31(LL_EXTI_LINE_20);
  /* USER CODE END RTC_IRQn 1 */
}

